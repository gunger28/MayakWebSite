                        Вибросито - это специальный аппарат, который предназначен для просеивания сыпучих материалов. 
                        Просеивание и отделение материалов происходит через специальную просеивающую поверхность с определенной величиной щелей (отверствий). 
                         Разные модели данного устройства имеют разную точность просеивания и отделения сыпучих веществ по их размеру.
Рабочий стол является основной составляющей частью вибрационного сита. 
На рабочую плиту устанавливаються просеивающие элементы. 
Колебательные движения плиты рабочего стола заставляют части сыпучего материала просеиваться через отверствия просеивающей поверхности.
<br>

</p>
<p class="text__text">


Вибросито состоит из рамы, на которой на резиновых амортизаторах установлен вибростол. 
Вибростол имеет два эксцентрика, верхний и нижний. На вибростоле шпильками закрепляется просеиватель. 
Просеиватель состоит из трех проставок и двух дек с сетками. 
Эксцентрики, установленые на одном валу, приводятся во вращение от электродвигателя посредством мягкой муфты. 
Вибрация создается на вибростоле грузиками эксцентриков. 
Передается на просеиватель, в котором продукт при перемещении на сетках делится на фракции и выходит через сходы. 
Амплитуда колебаний регулируется взаимным положением грузиков. 
При сведении грузиков происходит увеличение амплитуды, при разведении - уменьшение. Между собой грузики скрепляются стяжным винтом. 
Верхний эксцентрик создает горизонтальные колебания, нижний эксцентрик - вертикальные. 
